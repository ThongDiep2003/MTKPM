
class Computer {
    public void turnOn() {
        System.out.println("Bật máy tính");
    }

    public void turnOff() {
        System.out.println("Tắt máy tính");
    }

    public void volumeUp() {
        System.out.println("Tăng âm lượng");
    }

    public void volumeDown() {
        System.out.println("Giảm âm lượng");
    }

    public void brightnessUp() {
        System.out.println("Tăng độ sáng màn hình");
    }

    public void brightnessDown() {
        System.out.println("Giảm độ sáng màn hình");
    }
}